<p>¡Bienvenido a OpenSeaMap!
El objetivo de OpenSeaMap! es añadir información sobre turismo náutico-recreativo para marineros interesados en OpenStreetMap y presentarla de manera agradable y útil. Así, se incluye luces marítimas, boyas y otras señales de navegación, información portuaria, talleres de reparación, suministros para embarcaciones y mucho más. También se incluyen otros servicios asociados tales como tiendas, restaurantes y lugares de interés.
