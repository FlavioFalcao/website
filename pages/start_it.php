<p>Benvenuto in OpenSeaMap!
Lo scopo del progetto OpenSeaMap è di fornire informazioni nautiche e turistiche al navigatore interessato al progetto OpenStreetMap e di presentarle nel modo più utile e comprensibile. Queste informazioni includono fari, boe e altri segnali marittimi, informazioni sul porto, luoghi di riparazione, rivenditori di materiale marittimo e molto altro. Sono inclusi anche serivizi quali negozi, ristoranti e luoghi di interesse.</p>
